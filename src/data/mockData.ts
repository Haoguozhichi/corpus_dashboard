/** 生成模拟训练曲线（带噪声的收敛曲线） */
function generateCurve(
  points: number,
  start: number,
  end: number,
  noise: number,
  decreasing: boolean,
): number[] {
  const curve: number[] = [];
  for (let i = 0; i < points; i++) {
    const progress = i / (points - 1);
    const base = decreasing
      ? start - (start - end) * Math.sqrt(progress)
      : start + (end - start) * Math.sqrt(progress);
    const jitter = (Math.random() - 0.5) * noise * (1 - progress * 0.8);
    curve.push(Number(Math.max(0, base + jitter).toFixed(6)));
  }
  return curve;
}

